    public void autoID() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline reservation","root","");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(id) from ticketdetails");
            rs.next();
            rs.getString("MAX(id)");
            if (rs.getString("MAX(id)") == null) {
                txtTicketNumber.setText("IT001");
            } else {
                long id = Long.parseLong(rs.getString("MAX(id)").substring(2, rs.getString("MAX(id)").length()));
                id++;
                txtTicketNumber.setText("IT"+String.format("%03d", id));
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(bookTickets.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
