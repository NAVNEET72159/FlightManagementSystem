    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String source = txtSource.getSelectedItem().toString();
        String destination = txtDestination.getSelectedItem().toString();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline reservation","root","");
            pst = con.prepareStatement("select * from flight_details where source = ? and destination = ?");
            pst.setString(1, source);
            pst.setString(2, destination);
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            int c;
            c = rsmd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()) {
                Vector v = new Vector();
                for (int i = 1; i <= c; i++) {
                    v.add(rs.getString("flightID"));
                    v.add(rs.getString("flightName"));
                    v.add(rs.getString("source"));
                    v.add(rs.getString("destination"));
                    v.add(rs.getString("date"));
                    v.add(rs.getString("arrivalTime"));
                    v.add(rs.getString("departureTime"));
                    v.add(rs.getString("convenience"));
                }
                
                dtm.addRow(v);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(bookTickets.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

